                <h2>Lion cub born at Assiniboine Park Zoo</h2>
                
                <p>There&rsquo;s a new critter at the Assiniboine Park Zoo. And we&rsquo;re betting it&rsquo;s pretty cute.</p>

                <p>The zoo announced Wednesday that six-year-old African lion Kayia gave birth early March 30 to a healthy cub. Its sex has yet to be determined.</p>
                
                <p>Kayia and her five-year-old mate Xerces have been at the zoo since March 2010.</p>
                
                <p>&ldquo;Everyone at the Assiniboine Park Zoo is thrilled about the new addition to our Zoo family,&rdquo; said Tim Sinclair-Smith, director of zoological operations. &ldquo;The Zoo has a very successful birth rate and we are pleased that it continues with the healthy delivery of new lion club.&rdquo;</p>
                
                <center><img src="img/articles/lion.jpeg" width="400" alt="Lion" /></center>
                
                <p>The cub&rsquo;s parents are on loan to the zoo from a private facility in Ontario and will be returned there at some point along with their cub.</p>
                
                <p>Visitors to Winnipeg&rsquo;s zoo will have to wait about six to eight weeks to see the cub in the lion exhibit.</p>
                
                <p><strong>NEW ENTRANCE</strong></p>
                
                <p>The Assiniboine Park Conservancy also announced Wednesday that there&rsquo;s a new way to get into its zoo.</p>
                
                <p>Starting Friday, the main entrance will be at the south gate, located at 2595 Roblin Blvd.</p>
                
                <p>The former main entrance at the east gate, located within the park, will be closed for general admission and used only for large groups and education programs.</p>
                
                <p>The zoo gift shop next to the east gate can be accessed from inside or outside the zoo.</p>
                
                <p><strong>NATURE PLAYGROUND<br />
                
                </strong></p>
                
                <p>The zoo's 42-seat tram, a new addition last year, is again up and running and will move along a new route that starts just inside the south gate entrance, making several stops around the zoo</p>
                
                <p>The Conservancy also said portions of Assiniboine Park's nature playground that were closed over the winter will gradually re-open as the weather improves. The basket swings, crow's nest, and slithering slides will open Friday but the washrooms and the sand and water play area will remain closed until further notice.</p>
                
                <p>The nature playground is located just west of the pavilion and south of the duck pond.</p>

