            <h1>About Ron R. Schuler, MLA</h1>
            <a class="close"></a>
            <br clear="all" />
            
            <div class="overlay_content" id="overlay_content" style="display:block;">

<p>Ron R. Schuler is a Progressive Conservative Member of the Manitoba Legislature (MLA) representing East St. Paul, West St. Paul and parts of Springfield including Oak Bank and Dugald. He is currently the Critic for the Workers Compensation Board, Manitoba Liquor Control Commission, and Sport in Manitoba.</p>

<p>Ron was born in Winnipeg, Manitoba and attended John M. King Elementary School, before moving to Benton Harbor, Michigan, where he attended Millburg Elementary School in Berrien County. In 1976, he moved back to Winnipeg, where he attended Elmwood Junior High and graduated from Elmwood High School in 1981. He is the youngest son of Wanda and Reinhold Schuler, with four sisters and one brother.</p>
 
<p>A University of Manitoba graduate in 1987, with a major in international relations and a minor in sociology, Ron was a junior executive at the T. Eaton Company in 1979-84. He went on to become a small business owner and co-owner of Christmas Traditions, Espresso Junction Inc. and Gingerbread World Inc.</p>
 
<p>Ron also served as Chair of the Manitoba Intercultural Council from 1991 to 1993. In his first term on the River East School Board, Schuler served as Chairman of the Education and Policy Committee. He advocated a zero violence tolerance policy on issues affecting the safety of students and staff. Ron strongly supported increased technology funding and division-wide upgrades to classroom computers, and also strongly supported heritage language programs offered in the divisions.</p>
 
<p>On October 28th, Ron was re-elected as a school trustee for the River East School Division, Ward Three in the 1998 election. This re-election led him to being appointed to serve as Chairman of the Board of Trustees in November 1998. He resigned from his position as school trustee on September 23, 1999 to pursue his career in provincial politics.</p>
 
<p>During Ron's first term as a Member of the Legislative Assembly (MLA), Gary Filmon, Leader of the Progressive Conservative Party of Manitoba appointed Ron as the PC Caucus Labour & Immigration Critic.</p>
 
<p>The voters of Springfield re-elected Ron as their MLA on June 3, 2003 by a much greater, despite the poor performance of the Progressive Conservative Party of Manitoba candidates in most of Manitoba. Stuart Murray appointed him as the PC Caucus Critic for Energy, Science and Technology, and the Chief Critic for the Kyoto Accord, the Civil Service Commission, and Lotteries and Gaming.</p>
 
<p>On February 24, 2006, Ron announced that he would seek the leadership of the Progressive Conservative Party of Manitoba, but unfortunately was not elected on April 29, 2006.</p>
 
<p>For a third consecutive term, Ron was re-elected as a Member of the Legislative Assembly for Springfield on May 22, 2007 and in September of 2007, he was appointed Critic for Education, Citizenship and Youth, as well as Caucus Whip by Leader Hugh McFadyen. On February 4, 2010, Ron was made the Critic for Housing and Community Development and Sports.</p>
 
<p>Recently, Ron was nominated to represent the Progressive Conservative Party in the 2011 Manitoba provincial election, held on October 4, in the newly created constituency of St Paul. With more support than the constituency has ever seen previously, Ron won a 4th consecutive term and became the very first MLA from St. Paul.</p>
 
<p>Ron is an active community volunteer and has given his time to Big Brothers, Citizens on Patrol, Citizens Against Impaired Driving, the Kidney Foundation of Manitoba, Max Reinhardt Theatre Canada, the Folk Arts Council, the German Canadian Heritage Foundation and the Manitoba Inter-cultural Council. Ron and his children are all active in their schools, community, youth sports, and their church.</p>

